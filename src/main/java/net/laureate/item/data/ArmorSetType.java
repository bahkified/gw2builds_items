package net.laureate.item.data;

public class ArmorSetType {

    private ArmorType helm;
    private ArmorType shoulders;
    private ArmorType chest;
    private ArmorType gloves;
    private ArmorType leggings;
    private ArmorType boots;

    public ArmorType getHelm() {
        return helm;
    }

    public void setHelm(ArmorType helm) {
        this.helm = helm;
    }

    public ArmorType getShoulders() {
        return shoulders;
    }

    public void setShoulders(ArmorType shoulders) {
        this.shoulders = shoulders;
    }

    public ArmorType getChest() {
        return chest;
    }

    public void setChest(ArmorType chest) {
        this.chest = chest;
    }

    public ArmorType getGloves() {
        return gloves;
    }

    public void setGloves(ArmorType gloves) {
        this.gloves = gloves;
    }

    public ArmorType getLeggings() {
        return leggings;
    }

    public void setLeggings(ArmorType leggings) {
        this.leggings = leggings;
    }

    public ArmorType getBoots() {
        return boots;
    }

    public void setBoots(ArmorType boots) {
        this.boots = boots;
    }
}
